    /**
     *
     * @param sourceText
     * @param shift
     * @return
     */
    public String encryptText(String sourceText, int shift){
        StringBuilder encryptedText= new StringBuilder();
        char sourceTextArray[] = sourceText.toCharArray();
        for (char character:sourceTextArray){
            char shiftedCharacter = character;
            shiftedCharacter+= (char)shift;
            encryptedText.append(shiftedCharacter);
        }
        return encryptedText.toString();
    }
    
    /**
     *
     * @param sourceText
     * @param shift
     * @return
     */
    public String decryptText(String sourceText, int shift){
        StringBuilder decryptedText= new StringBuilder();
        char sourceTextArray[] = sourceText.toCharArray();
        for (char character:sourceTextArray){
            char shiftedCharacter = character;
            shiftedCharacter-= (char)shift;
            decryptedText.append(shiftedCharacter);
        }
        return decryptedText.toString();
    }

